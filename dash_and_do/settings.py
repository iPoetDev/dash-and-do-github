#        - CSRF Cookie Path is /
#        - CSRF Cookie SameSite is Lax
#        - CSRF Cookie Header Name is HTTP_X_CSRFTOKEN
#        - CSRF Cookie Failure View is django.views.csrf.csrf_failure
